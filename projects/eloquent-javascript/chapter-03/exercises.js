function min(num1, num2) {
return Math.min(num1, num2)
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {

  if (n == 0) {
  return true;
  }
  else if (n == 1) { 
  return false;
  } else if (n < 0) { 
  return isEven(-n);
  }
  else {
  return isEven(n - 2);
  }

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
 
 let collection = [];
 for (let i =0; i < string.length; i++) {
   if (string[i].toUpperCase() === char.toUpperCase()) {
     collection.push(string[i]);
   }

 }
 return collection.length;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
let Bcollection = [];
 for (let i =0; i < string.length; i++) {
   if (string[i].toUpperCase() === "b".toUpperCase()) {
     Bcollection.push(string[i]);
   }

 }
 return Bcollection.length;
}

